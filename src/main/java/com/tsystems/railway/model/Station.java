package com.tsystems.railway.model;


import javax.persistence.*;

@Entity
@Table(name ="stations")
public class Station {

    @Id
    @GeneratedValue
    @Column(name = "station_id")
    private long id;

    @Column(name = "name")
    private String name;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Station{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
